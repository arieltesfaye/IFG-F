                                <p>Our Story</p>
                                <h2>The Spark of Change</h2>
                            </div>
                        </div>                                              
                        <p>
                            The vision for the Impact for Good Foundation (IFG-F) began with a group of dedicated individuals who recognized the need for a foundation that prioritized sustainable development, community empowerment, and social equity. 
                        </p>
                        <p>
                            Stacy Waruguru Muriithi, a civic leader and passionate advocate for climate action, gathered a small group of like-minded individuals, including Loise Atieno and Navrouze Desouza Odhiambo, to explore how they could make a meaningful impact.
                        </p>
                        <p>
                            Drawing inspiration from similar movements across Africa, Stacy and her team envisioned a foundation deeply committed to uplifting vulnerable communities through locally led initiatives.
                        </p>
                        <h3>IFG-F Is Born</h3>
                        <p>
                            IFG-F was officially registered as a community-driven organization by its founding members, supported by leaders in Kenya’s financial and social sectors.
                        </p>
                        <p>
                            With early support from community partners and an initial contribution from Stacy’s and Navrouze’s own families, IFG-F began with a modest start but a powerful mission: to create sustainable opportunities for economic empowerment, address climate change, and advocate for gender equality.
                        </p>
                        <p>
                            By reaching out to communities in Kenya's 47 counties and beyond, IFG-F continues to partner with individuals and groups who share the vision of long-term positive change.
                        </p>
                        <p>
                            With each new initiative, from educational programs to climate advocacy, IFG-F embodies its commitment to creating lasting impacts and building resilient communities.
                        </p>

                        <h4>Join the Movement</h4>
                        <p>
                            You can be a part of the change by supporting IFG-F’s climate action initiatives. Your donation will go directly towards funding sustainable solutions and ensuring that vulnerable communities can continue to thrive despite the challenges of climate change.
                        </p>
                        <p>
                            Stay connected with us on social media to follow our progress and learn more about how you can contribute to our mission of creating a better future for all.
                        </p>    
